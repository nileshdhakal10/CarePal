package com.example.vitatsignproject;

public class VitalSign
{
    int blPressure, blSugar, choHDL, choLDL, trigly;

    public VitalSign() {
    }

    public int getBlPressure() {
        return blPressure;
    }

    public void setBlPressure(int blPressure) {
        this.blPressure = blPressure;
    }

    public int getBlSugar() {
        return blSugar;
    }

    public void setBlSugar(int blSugar) {
        this.blSugar = blSugar;
    }

    public int getChoHDL() {
        return choHDL;
    }

    public void setChoHDL(int chOHDL) {
        choHDL = chOHDL;
    }

    public int getChoLDL() {
        return choLDL;
    }

    public void setChoLDL(int chOLDL) {
        choLDL = chOLDL;
    }

    public int getTrigly() {
        return trigly;
    }

    public void setTrigly(int Trigly) {
        trigly = Trigly;
    }
}

